    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            int num = Integer.parseInt (jTextFieldNumero.getText());
            int de  = Integer.parseInt(jTextFieldDIni.getText());
            int a   = Integer.parseInt(jTextFieldDFin.getText());
            
            StringBuffer resultados = new StringBuffer();
            
            for (int i = 0; i < num; i++) {
                resultados.append((int)(Math.random()*a + de));
                resultados.append (" ");
            }
            // Mostramos el resultado en el chat de todos
            enviarDados(resultados, num, de, a);
        }
        catch (NumberFormatException ex) {
            
        }
    }//GEN-LAST:event_jButton1ActionPerformed
    
    private void enviarDados (StringBuffer resultados, int casos, int ini, int fin) {      
        Calendar calendario = new GregorianCalendar();
         Main.msgCliente("[" + calendario.get(Calendar.HOUR_OF_DAY) + ":" +
                    calendario.get(Calendar.MINUTE) + "] Dados de " +
                    name + " (del " + ini + " al " + fin + "): " +
                    resultados.toString());
    }
    
    private void enviar(String msg) {
        if (msg.length() > 0) {
            Calendar calendario = new GregorianCalendar();
            Main.msgCliente("[" + calendario.get(Calendar.HOUR_OF_DAY) + ":" +
                    calendario.get(Calendar.MINUTE) + "] " +
                    name + ": " + msg);
        }
    }
    
