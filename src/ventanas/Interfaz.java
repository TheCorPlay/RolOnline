    private void jListJugadoresMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jListJugadoresMouseClicked
        // Si hacemos doble click
        if (evt.getClickCount() > 1) {
            new Thread () {
                public void run() {
                    if (Main.perfil != null)
                        Main.perfil.dispose();
                    
                    if (!jListJugadores.getSelectedValue().equals(name)) {
                        Main.perfil = new Perfil(jListJugadores.getSelectedValue());
                        Main.perfil.setVisible(true);
                    }
                    else {
                        // Abrimos la ventana de interfaz propia
                        throw new UnsupportedOperationException("Not supported yet.");
                    }
                    //while (Main.client == null) {}
                    //Main.darNombreCliente(jTextField1.getText());
                }
            }.start();
        }
    }//GEN-LAST:event_jListJugadoresMouseClicked

