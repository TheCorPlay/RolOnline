    private void enviar() {
        Calendar calendario = new GregorianCalendar();
        String msg ="[" + calendario.get(Calendar.HOUR_OF_DAY) + ":" +
                    calendario.get(Calendar.MINUTE) + "] " +
                    Main.name + ": " +jTextFieldEnviar.getText();
        mostrarMsg (msg);
        
        Main.msgPrivateCliente(msg, Main.name, destinatario);
    }
    
    public void mostrarMsg(String msg) {
        jTextAreaChat.append(msg + '\n');
        //System.out.println(msg);
    }
    
    public void log (String msg) {
        jTextAreaChat.setText(msg);
    }
